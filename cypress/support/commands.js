Cypress.Commands.add('withErrorHandling', (action, errorMessage) => {
    const timestamp = new Date().toISOString().replace(/[:.]/g, "-");
  
    cy.once('fail', (error) => {
      cy.screenshot(`error-${timestamp}`, { capture: 'runner' });
      throw new Error(errorMessage || error.message);
    });
  
    action();
  });